void BH1750_Init(void)
{
    // B?t ngu?n cho BH1750
    uint8_t power_on_cmd = BH1750_POWER_ON;
    HAL_I2C_Master_Transmit(&hi2c1, BH1750_ADDRESS, &power_on_cmd, 1, HAL_MAX_DELAY);
}

float BH1750_ReadLightLevel(void)
{
    uint8_t mode_cmd = BH1750_ONE_TIME_HRES_MODE;
    uint8_t buffer[2];

    // G?i l?nh do m?t l?n v?i d? phân gi?i cao
    HAL_I2C_Master_Transmit(&hi2c1, BH1750_ADDRESS, &mode_cmd, 1, HAL_MAX_DELAY);

    // Ð?i d? c?m bi?n th?c hi?n do (thu?ng m?t kho?ng 180ms)
    HAL_Delay(180);

    // Ð?c d? li?u ánh sáng t? c?m bi?n BH1750
    HAL_I2C_Master_Receive(&hi2c1, BH1750_ADDRESS, buffer, 2, HAL_MAX_DELAY);

    // Chuy?n d?i d? li?u t? 2 byte thành giá tr? cu?ng d? ánh sáng (lux)
    uint16_t raw_value = (buffer[0] << 8) | buffer[1];
    float lux = raw_value / 1.2;  // Theo tài li?u c?a BH1750, chia cho 1.2 d? ra giá tr? lux
    return lux;
}

void Transmit_Light_Level(float lux)
{
    char uart_buffer[50];
    snprintf(uart_buffer, sizeof(uart_buffer), "Light Level: %.2f lux\r\n", lux);
    HAL_UART_Transmit(&huart1, (uint8_t*)uart_buffer, strlen(uart_buffer), HAL_MAX_DELAY);
}
